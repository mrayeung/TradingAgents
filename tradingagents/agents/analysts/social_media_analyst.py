from langchain_core.prompts import ChatPromptTemplate, MessagesPlaceholder
from tradingagents.agents.utils.agent_utils import build_instrument_context, get_language_instruction, get_news
from tradingagents.agents.utils.news_data_tools import get_social_sentiment
from tradingagents.dataflows.config import get_config


def create_social_media_analyst(llm):
    def social_media_analyst_node(state):
        current_date = state["trade_date"]
        instrument_context = build_instrument_context(state["company_of_interest"])

        tools = [
            get_news,
            get_social_sentiment,
        ]

        system_message = (
            "You are a social media and sentiment analyst tasked with analyzing social media activity, "
            "crowd sentiment, and recent company news for a specific stock over the past week. "
            "Use get_news(ticker, start_date, end_date) to retrieve the latest company news headlines. "
            "Use get_social_sentiment(ticker, start_date, end_date) to retrieve daily Reddit and "
            "StockTwits mention counts and positive/negative sentiment scores from Finnhub. "
            "Analyze: (1) the trend in daily mention volume — rising attention often precedes a move; "
            "(2) the ratio of positive to negative sentiment and how it shifts day-to-day; "
            "(3) alignment or divergence between social sentiment and the news narrative. "
            "Write a comprehensive report with specific, actionable insights and supporting data. "
            "Try to cover all available sources — news, Reddit, StockTwits."
            + """ Make sure to append a Markdown table at the end of the report to organize key points in the report, organized and easy to read."""
            + get_language_instruction()
        )

        prompt = ChatPromptTemplate.from_messages(
            [
                (
                    "system",
                    "You are a helpful AI assistant, collaborating with other assistants."
                    " Use the provided tools to progress towards answering the question."
                    " If you are unable to fully answer, that's OK; another assistant with different tools"
                    " will help where you left off. Execute what you can to make progress."
                    " If you or any other assistant has the FINAL TRANSACTION PROPOSAL: **BUY/HOLD/SELL** or deliverable,"
                    " prefix your response with FINAL TRANSACTION PROPOSAL: **BUY/HOLD/SELL** so the team knows to stop."
                    " You have access to the following tools: {tool_names}.\n{system_message}"
                    "For your reference, the current date is {current_date}. {instrument_context}",
                ),
                MessagesPlaceholder(variable_name="messages"),
            ]
        )

        prompt = prompt.partial(system_message=system_message)
        prompt = prompt.partial(tool_names=", ".join([tool.name for tool in tools]))
        prompt = prompt.partial(current_date=current_date)
        prompt = prompt.partial(instrument_context=instrument_context)

        chain = prompt | llm.bind_tools(tools)

        result = chain.invoke(state["messages"])

        report = ""

        if len(result.tool_calls) == 0:
            report = result.content

        return {
            "messages": [result],
            "sentiment_report": report,
        }

    return social_media_analyst_node
